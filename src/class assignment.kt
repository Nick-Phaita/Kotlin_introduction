fun main() {
    val num1 = 9
    val num2 = 4
    println("The sum is "+(num1+num2))
    //this is addition

    println("The difference is "+(num1-num2))
    //this is subtraction

    println("The product is "+(num1*num2))
    //this is multiplication

    println("The quotient is "+(num1/num2))
    //this is division

    println("The modulus is "+(num1%num2))
    //this is modulus
}

